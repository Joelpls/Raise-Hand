package RecyclerViews;

public class ListItemTeacherNotifications {

    private String head;
    private String desc;

    public ListItemTeacherNotifications(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
